;
;                                   Function:           LoadGLProc
;                                   Description:        Load an OpenGL function from the registry.
;
;                                   Parameters:
;                                       rcx     -       Pointer to a string that contains the name of the function
;
;                                   Returns:            A valid pointer to the function if successful, otherwise returns 0.
;
function                            LoadGLProc

                                    ; ------------- [Setup stack frame] -------------------------------------

                                    push            rbp
                                    mov             rbp, rsp
                                    sub             rsp, 32

                                    ; ------------- [Save name string] --------------------------------------

                                    mov             [rbp + 16], rcx

                                    ; ------------- [Get function pointer from opengl32.dll] ----------------

                                    mov             rcx, [GLHandle]
                                    mov             rdx, [rbp + 16]
                                    call            GetProcAddress

                                    ; ------------- [Check if the function pointer is 0] --------------------

                                    test            rax, rax
                                    jnz             .Return

                                    ; ------------- [Get function pointer from WGL] -------------------------

                                    mov             rcx, [rbp + 16]
                                    call            wglGetProcAddress

.Return:

                                    ; ------------- [Restore stack frame] -----------------------------------

                                    mov             rsp, rbp
                                    pop             rbp

                                    ; ------------- [Return] ------------------------------------------------

                                    ret

