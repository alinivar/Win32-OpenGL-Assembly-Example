%define __STR(&name) name

%macro GLAPI 1
    global FnPtr_%1
    section .data
    FnPtr_%1:   dq 0
    section .rdata
    Name_%1:    db __STR(%1), 0
%endmacro

%macro LOADGL 1
    mov         rcx, Name_%1
    call        LoadGLProc
    mov         [FnPtr_%1], rax
%endmacro

